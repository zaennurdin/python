# String concatenation
for i in range(5):
    print("The square of " + str(i) + " is " + str(i * i))

# String interpolation
for i in range(8):
    print(f"{i * i} is The square of {i}")

for x in range(99, 0, -1):
    print(f"{x} botol wine di lemari")
    print(f"{x} botol wine")
    print(f"{x} jual satu di toko")
    print(f"{x - 1} botol wine di lemari")
